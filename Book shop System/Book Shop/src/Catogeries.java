    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            // TODO add your handling code here:
            String colmName = "";
            String keysersh = jTextField2.getText();
            if(jComboBox1.getSelectedItem().equals("اسم الفئة"))
                colmName = "categoryName";
            else if(jComboBox1.getSelectedItem().equals("وصف الفئة"))
                colmName = "categoryDiscruption";
            else if(jComboBox1.getSelectedItem().equals("الرقم التعريفى للفئه"))
                colmName = "categoryId";
            Function.Category cat = new Function.Category();
            if(!(jTextField2.getText().equals("")))
                jTable1.setModel(cat.sersh(keysersh,colmName));
            else
                JOptionPane.showMessageDialog(rootPane, "please Enter wordto searsh");
        } catch (SQLException ex) {
            Logger.getLogger(Catogeries.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            // TODO add your handling code here:
            int row_num = jTable1.getSelectedRow();
            Classes.Category cat = new Classes.Category();
            cat.setCategoryId(Integer.parseInt(String.valueOf(jTable1.getValueAt(row_num, 0))));
            cat = (new Function.Category()).Showrow(cat.getCategoryId());
            jTextField1.setText(String.valueOf(cat.getCategoryId()));
            jTextField3.setText(cat.getCategoryName());
            text.setText(cat.getCategoryDiscruption());
        } catch (SQLException ex) {
            Logger.getLogger(Catogeries.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

    private void btndeletActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndeletActionPerformed
        // TODO add your handling code here:
        String s = String.valueOf(jTextField1.getText());
        if(!s.equals(""))
        {
            try {
                int id = Integer.parseInt(s);
                (new Function.Category()).delet(id);
                JOptionPane.showMessageDialog(rootPane,"Delet succes");
                btnDumpActionPerformed(evt);
            } catch (SQLException ex) {
                Logger.getLogger(Catogeries.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "selsct category");
        }
                           
    }//GEN-LAST:event_btndeletActionPerformed

