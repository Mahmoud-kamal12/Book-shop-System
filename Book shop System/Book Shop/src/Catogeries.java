    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        // TODO add your handling code here:
        Function.Category catfunction = new Function.Category();
        Classes.Category ca = new Classes.Category();

        if(!(jTextField3.getText().equals("")&&text.getText().equals(""))){
            try {
                ca.setCategoryId(Integer.parseInt(String.valueOf(jTextField1.getText())));
                ca.setCategoryName(jTextField3.getText());
                ca.setCategoryDiscruption(text.getText());
                catfunction.update(ca);
                JOptionPane.showMessageDialog(rootPane, "Category update secces");
                jTable1.setModel((new Function.Category().showAll()));
            } catch (SQLException ex) {
                Logger.getLogger(Catogeries.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else
        {
           JOptionPane.showMessageDialog(rootPane, "insert category info ");
        }
    }//GEN-LAST:event_btnupdateActionPerformed

